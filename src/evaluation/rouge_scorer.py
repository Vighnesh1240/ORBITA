"""
ROUGE Score Implementation for ORBITA

ROUGE (Recall-Oriented Understudy for Gisting Evaluation) is the
standard metric for evaluating text summarization quality.

We implement three variants:
    ROUGE-1: Unigram overlap (individual word matches)
    ROUGE-2: Bigram overlap (two-word phrase matches)
    ROUGE-L: Longest Common Subsequence (captures sentence structure)

Why ROUGE?
    It is the standard metric used in:
    - Summarization papers (CNN/DailyMail benchmark)
    - News synthesis evaluation
    - All major NLP conferences

Note on Reference Summaries:
    For evaluation, you need human-written reference summaries.
    We provide a small set of reference summaries for benchmark topics.
    For new topics, ROUGE cannot be computed without a reference.

Reference:
    Lin (2004) "ROUGE: A Package for Automatic Evaluation of Summaries"
    ACL Workshop on Text Summarization Branches Out
"""

import re
from collections import Counter
from typing import Optional


def _tokenize(text: str) -> list:
    """
    Simple tokenizer: lowercase, remove punctuation, split on spaces.
    Matches standard ROUGE preprocessing.
    """
    if not text:
        return []

    # Lowercase
    text = text.lower()

    # Remove punctuation except apostrophes
    text = re.sub(r"[^\w\s']", " ", text)

    # Split and filter empty tokens
    tokens = [t.strip() for t in text.split() if t.strip()]

    return tokens


def _get_ngrams(tokens: list, n: int) -> Counter:
    """
    Extract n-grams from a token list and count frequencies.

    Example:
        tokens = ["the", "cat", "sat"]
        n = 2
        returns Counter({("the","cat"): 1, ("cat","sat"): 1})
    """
    ngrams = []
    for i in range(len(tokens) - n + 1):
        ngram = tuple(tokens[i:i + n])
        ngrams.append(ngram)
    return Counter(ngrams)


def _lcs_length(seq1: list, seq2: list) -> int:
    """
    Compute the length of the Longest Common Subsequence.
    Uses dynamic programming — O(m*n) time.

    This is the core of ROUGE-L.
    """
    m = len(seq1)
    n = len(seq2)

    # Create DP table
    dp = [[0] * (n + 1) for _ in range(m + 1)]

    for i in range(1, m + 1):
        for j in range(1, n + 1):
            if seq1[i - 1] == seq2[j - 1]:
                dp[i][j] = dp[i - 1][j - 1] + 1
            else:
                dp[i][j] = max(dp[i - 1][j], dp[i][j - 1])

    return dp[m][n]


def rouge_n(hypothesis: str, reference: str, n: int = 1) -> dict:
    """
    Compute ROUGE-N score.

    ROUGE-N measures n-gram overlap between hypothesis and reference.

    Formula:
        Precision = (matching n-grams) / (n-grams in hypothesis)
        Recall    = (matching n-grams) / (n-grams in reference)
        F1        = 2 * P * R / (P + R)

    Args:
        hypothesis: the text generated by ORBITA (synthesis)
        reference:  the human-written reference summary
        n:          n-gram size (1 for ROUGE-1, 2 for ROUGE-2)

    Returns:
        dict with precision, recall, f1 scores
    """
    hyp_tokens = _tokenize(hypothesis)
    ref_tokens = _tokenize(reference)

    if not hyp_tokens or not ref_tokens:
        return {"precision": 0.0, "recall": 0.0, "f1": 0.0}

    hyp_ngrams = _get_ngrams(hyp_tokens, n)
    ref_ngrams = _get_ngrams(ref_tokens, n)

    # Count overlapping n-grams
    # For each n-gram, take the minimum count between hyp and ref
    overlap = sum(
        min(hyp_ngrams[ng], ref_ngrams[ng])
        for ng in hyp_ngrams
        if ng in ref_ngrams
    )

    total_hyp = sum(hyp_ngrams.values())
    total_ref = sum(ref_ngrams.values())

    precision = overlap / total_hyp if total_hyp > 0 else 0.0
    recall    = overlap / total_ref if total_ref > 0 else 0.0
    f1        = (
        (2 * precision * recall) / (precision + recall)
        if (precision + recall) > 0 else 0.0
    )

    return {
        "precision": round(precision, 4),
        "recall":    round(recall,    4),
        "f1":        round(f1,        4),
    }


def rouge_l(hypothesis: str, reference: str) -> dict:
    """
    Compute ROUGE-L score using Longest Common Subsequence.

    ROUGE-L captures sentence-level structural similarity.
    It does not require consecutive matches like ROUGE-N.

    Args:
        hypothesis: ORBITA's synthesis text
        reference:  human reference summary

    Returns:
        dict with precision, recall, f1 scores
    """
    hyp_tokens = _tokenize(hypothesis)
    ref_tokens = _tokenize(reference)

    if not hyp_tokens or not ref_tokens:
        return {"precision": 0.0, "recall": 0.0, "f1": 0.0}

    lcs = _lcs_length(hyp_tokens, ref_tokens)

    precision = lcs / len(hyp_tokens) if hyp_tokens else 0.0
    recall    = lcs / len(ref_tokens)  if ref_tokens  else 0.0
    f1        = (
        (2 * precision * recall) / (precision + recall)
        if (precision + recall) > 0 else 0.0
    )

    return {
        "precision": round(precision, 4),
        "recall":    round(recall,    4),
        "f1":        round(f1,        4),
    }


def compute_all_rouge(
    hypothesis: str,
    reference:  str,
) -> dict:
    """
    Compute all ROUGE variants at once.

    This is the main function to call from the evaluator.

    Args:
        hypothesis: ORBITA synthesis report
        reference:  human-written reference summary

    Returns:
        dict with rouge_1, rouge_2, rouge_l scores
    """
    return {
        "rouge_1": rouge_n(hypothesis, reference, n=1),
        "rouge_2": rouge_n(hypothesis, reference, n=2),
        "rouge_l": rouge_l(hypothesis, reference),
    }


