def get_reference_summary(topic: str) -> Optional[str]:
    """
    Retrieve the reference summary for a topic.

    Args:
        topic: topic string

    Returns:
        reference summary string, or None if not available
    """
    topic_lower = topic.lower().strip()

    # Exact match
    if topic_lower in REFERENCE_SUMMARIES:
        return REFERENCE_SUMMARIES[topic_lower]

    # Partial match
    topic_words = set(topic_lower.split())
    best_match  = None
    best_overlap = 0

    for key in REFERENCE_SUMMARIES:
        key_words = set(key.split())
        overlap   = len(topic_words & key_words)
        if overlap > best_overlap:
            best_overlap = overlap
            best_match   = key

    if best_match and best_overlap >= 2:
        return REFERENCE_SUMMARIES[best_match]

    return None